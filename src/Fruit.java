// 水果类  
public class Fruit {
    String name;  
    double pricePerJin;  
  
    public Fruit(String name, double pricePerJin) {  
        this.name = name;  
        this.pricePerJin = pricePerJin;  
    }  
  
    public String getName() {  
        return name;  
    }  
  
    public double getPricePerJin() {  
        return pricePerJin;  
    }  
}  